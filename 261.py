# Python for loop with Range

# This code uses a Python for loop with index in conjuction with the range() function to generate a sequence of numbers starting from 0, up to (but not including) 10, and with a step size of 2. For each numebr in the sequence, the loop prints its value using the print() function. The output will show the numbers 0, 2, 4, 6, and 8.

for i in range(0, 10, 2):
    print(i)