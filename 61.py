# Sequence Data Types in Python

# List Data Type = Lists are just like arrays, declared in other languages which is an ordered collection of data. It is very flexible as the items in a list do not need to be of the same type.

# Creating a List in Python = Lists in Python can be created by just placing the sequence inside the square Brackets[].

List = []
print("\nInitial blank List: ")
print(List)
List = ["GeeksForGeeks"]
print("\nList with the use of String: ")
print(List)
List = ["Geeks", "For", "Geeks"]
print("\nList containing multiple values: ")
print(List[0])
print(List[2])
List = [["Geeks", "For"], ["Geeks"]]
print("\nMulti-Dimensional List: ")
print(List)