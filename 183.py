# Python Operators.

# In Python Programming, Operators in general are used to perform operations on values and variables. These are standard symbols used for logical and arithmetic operations. 

# OPERATORS: These are the special symbols. Examples: +,-,*,/, etc.
# OPERAND: It is the value on which the operator is applied. 

a = float(input("\nEnter the Value of a: "))
b = float(input("Enter the Value of b: "))

Addition = a + b
Subtraction = a - b
Multiplication = a * b
Division = a / b
Floor_Division = a // b
Modulus = a % b
Exponential = a ** b

print("\nThe Addition of", a, "and", b, "is", Addition)
print("The Subtraction of", a, "and", b, "is", Subtraction)
print("The Multiplication of", a, "and", b, "is", Multiplication)
print("The Division of", a, "and", b, "is", Division)
print("The Floor_Division of", a, "and", b, "is", Floor_Division)
print("The Modulus of", a, "and", b, "is", Modulus)
print("The Exponential of", a, "and", b, "is", Exponential)

print("\n")