# Difference between For Loop and While Loop in Programming.

# -> Both of the For Loops and While Loops are control flow structures in programming that allow you to repeatedly execute a block of code.

# For Loop in Programming.

# -> The For Loop is used when you know in advance that how many times you want to execute the block of code.
# -> It iterates over a sequence (e.g., a list, string, or range) and executes the block of code for each item in the sequence.
# -> The Loop variable (variable) takes the value of each item in the sequence during each iteration.

# While Loop in Programming.

# -> The While Loop is used when we don't know in advance that how many times you want to execute the block of code. It continues to execute as long as the specified condition is true.
# -> It is important to make sure that the condition eventually becomes false; otherwise, the loop will run indefinitely, resulting in an infinite loop.

# In For Loop, the Initialization is declared within the loop structure and executed once at the beginning.

# In While Loop, the Initialization is declared outside the loop; should be done explicitly before the loop.