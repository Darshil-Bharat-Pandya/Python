# Ternary Operator in Python
# Syntax: true_value if condition else false_value
# Python Ternary if Else

# In this code we will compare and find the minimum number from the given two numbers using the Ternary Operators in Python and storing the result in a variable name 'min. If 'a'  is minimum, the value of 'a' will be printed, else the value of 'b' will be printed.

a = 10
b = 20

min = "a is minimum" if a < b else "b is minimum"

print(min)