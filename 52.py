# <stt1> if <condition> else <stt2>

food = input("food: ")
print("sweet") if food == "cake" or food == "jalebi" else print("not sweet")

Tyres = input("Tyres: ")
print("Yes") if Tyres == "MRF" or Tyres == "Bridgestone" else print("No")

Fruits = input("Fruits: ")
print("Yes") if Fruits == "Apple" or Fruits == "Orange" else print("No")

Number = int(input("Please Enter the Number: "))
print("It's an Even Number") if (Number % 2) == 0 else print("It's an Odd Number")