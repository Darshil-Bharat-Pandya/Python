# Sequence Data Type in Python
# The sequence Data Type in Python is the ordered collection of similar or different Python data types. Sequences allow storing of multiple values in an organized and efficient fashion. There are several sequence data types of Python:
# 1. Python String
# 2. Python List
# 3. Python Tuple

# 1. Python Strings

String1 = 'Welcome to the Geeks World'
print("\nString with the use of Single Quotes: ")
print(String1)

String1 = "I am a Geek"
print("\nString with the use of Double Quotes: ")
print(String1)
print(type(String1))

String1 = '''I am a Geek and I live in a world of "Geeks"'''
print("\nString with the use of Triple Quotes: ")
print(String1)
print(type(String1))

String1 = '''Geeks
             For
             Life'''
print("\nCreating a multiline String: ")
print(String1)