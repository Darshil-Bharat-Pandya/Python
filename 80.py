# Example of An Assignment Operator

Num1 = int(input("\nPlease Enter the Value in Num1: "))
Num1 += 5
print("\nAddition: ", Num1)

Num2 = int(input("\nPlease Enter the Value in Num2: "))
Num2 -= 5
print("\nSubtraction: ", Num2)

Num3 = int(input("\nPlease Enter the Value in Num3: "))
Num3 *= 5
print("\nMultiplication: ", Num3)

Num4 = int(input("\nPlease Enter the Value in Num4: "))
Num4 /= 5
print("\nDivision: ", Num4)

Num5 = int(input("\nPlease Enter the Value in Num5: "))
Num5 %= 5
print("\nModulo Operator: ", Num5)

Num6 = int(input("\nPlease Enter the Value in Num6: "))
Num6 **= 5
print("\nPower Operator: ", Num6)
