# Practice Question
# Write a Program in Python to Input 2 floating point numbers and also print their Average.

# Entering the Values in the Variables Num1 and Num2.
Num1 = float(input("\nPlease Enter the Value of Num1: "))
Num2 = float(input("\nPlease Enter the Value of Num2: "))

# Logic to print the Average of two numbers.
Average = (Num1 + Num2) / 2

# Printing the Values.
print("\nThe Average of Num1 and Num2 is", Average)