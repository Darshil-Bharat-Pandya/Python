# Input the Values in Both A and B.
A = int(input("\nPlease Enter the Value of A: "))
B = int(input("\nPlease Enter the Value of B: "))

# if-else Conditions

# If the Value of A is larger than B.
if (A > B):
    print("\nThe Value of A is Larger than B")
# If the Value of A is Equal to B.
elif (A == B):
    print("\nThe Value of A is Equal to B")   
# If the Value of A is Smaller than B
else:
    print("\nThe Value of A is Smaller than B")     