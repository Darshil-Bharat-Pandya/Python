# Logical Operator

# AND Operator
# In And Operator, if both the values are True then and only it will be True otherwise if One value is False then it will be False.
val1 = True
val2 = True
print("AND Operator:", val1 and val2)

val1 = True
val2 = False
print("AND Operator2:", val1 and val2)

val1 = False
val2 = False
print("AND Operator3:", val1 and val2)