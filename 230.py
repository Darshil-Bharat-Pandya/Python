# For Loop In Python

# For loops are used for sequential traversal. For example: traversing a list or string or array etc. In Python, there is "for in" loop which is similar to foreach loop in other languages.

# Syntax:
# for iteration_var in sequence:
#     statements(s)

# The code uses a Python for loop that iterates the values from 0 to 3(not including 4), as specified by the range(0, n) construct. It will print the values of "i" in each iteration of the loop.

n = 4
for i in range(0, n):
    print(i)