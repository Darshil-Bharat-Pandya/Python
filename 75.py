# Example of Operators in Python

# Entering the Values
a = int(input("\nPlease Enter The Value of a: "))
b = int(input("\nPlease Enter The Value of b: "))

# Applying the Arithmetic Operators
print("\nSum =",a + b)
print("\nDifference =",a - b)
print("\nProduct =",a * b)
print("\nDivision =",a / b)
print("\nModulo Operator =",a % b)
print("\nPower Operator =",a ** b)