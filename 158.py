# List Data Type = Lists are just like arrays, declared in other languages which is an ordered collection of data. It is very flexible as the items in a list do not need to be of the same type. The Lists are Mutable in Nature, it means after creating it once we can modify it or change it.

List = []
print("\nAn Initial Blank List:", List)

List = ["GeeksForGeeks"]
print("\nA List with the use of a String:", List)

List = ["Geeks", "For", "Geeks"]
print("\nA List containing Multiple Elements:", List)
print(List[0])
print(List[1])
print(List[-1])

List = [["Geeks"], ["For"], ["Geeks"]]
print("\nA Multi-Dimensional List:", List)
print(List[0])
print(List[-2])
print(List[-1])

print("\n")