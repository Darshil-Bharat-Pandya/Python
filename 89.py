# Type Casting
# It is used to manually change the Datatype of a Value in Python. It is used in Python to change a Value from one Datatype to another Datatype.
a = "2"
b = 4.25
print(type(a))
print(type(b))

a = int("2")
b = 4.25
print(a + b)
print(type(a))
print(type(b))

a = float("2")
b = 4.25
print(a + b)
print(type(a))
print(type(b))

a = str(3.14)
print(a)
print(type(a))