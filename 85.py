# Logical Operator

# OR Operator
# If one value from the two values is True, then the OR Operator will be True.

val1 = False
val2 = False
print("\nOR Operator:", val1 or val2)

val3 = True
val4 = False
print("\nOR Operator2:", val3 or val4)

val5 = False
val6 = True
print("\nOR Operator3:", val5 or val6)