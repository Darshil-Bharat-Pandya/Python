# While Loop in Python
# In Python, a while loop is used to execute a block of statements repeatedly until a given condition is satified. When thr condition becomes false, the line immediately after the loop in the program is executed.

# Syntax: 
#   while expression:
#       statement(s)

count = 0
while (count < 3):
    count = count + 1
    print("Hello Geek")